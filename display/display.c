void drawDigit(int x, int y, int digit, enum colors col, enum colors bgcol)
{
    if (digit < 0 || digit > 9) return;
    const uint8_t *bitmap = digitBitmaps[digit];

    int row, colBit;
    for (row = 0; row < 12; row++) {
        uint8_t line = bitmap[row];
        for (colBit = 0; colBit < 8; colBit++) {
            if (line & (1 << (7 - colBit))) {
                // Pixel setzen, z.B. als 1x1 Rechteck
                drawPixel(3*(x + colBit), 3*(y + row), col);
            }
            else drawPixel(x + colBit, y + row, bgcol);
        }
    }
}

void drawDigit16x24(int x, int y, int digit, enum colors col, enum colors bgcol)
{
    if (digit < 0 || digit > 9) return;
    const uint8_t *bitmap = digitBitmaps16x24[digit];

    int row, colBit;
    for (row = 0; row < 24; row++) {
        uint8_t left = bitmap[row * 2];
        uint8_t right = bitmap[row * 2 + 1];
        uint16_t line = (left << 8) | right;  // 16 Bits = 1 Zeile

        for (colBit = 0; colBit < 16; colBit++) {
            if (line & (1 << (15 - colBit))) {
                drawPixel(x + colBit, y + row, col);
            }
            else drawPixel(x + colBit, y + row, bgcol);
        }
    }
}

void drawDigit32x50(int x, int y, int digit, enum colors col, enum colors bgcol)
{
    if (digit < 0 || digit > 9) return;
    const uint8_t *bitmap = digitBitmaps32x50[digit];
    int row, colBit;
    for (row = 0; row < 50; row++)
    {
        // 4 Bytes pro Zeile einlesen (MSB first)
        uint32_t line =
              (bitmap[row * 4 + 0] << 24)
            | (bitmap[row * 4 + 1] << 16)
            | (bitmap[row * 4 + 2] << 8)
            |  bitmap[row * 4 + 3];

        for (colBit = 0; colBit < 32; colBit++)
        {
            // höchstes Bit entspricht x+0
            if (line & (1u << (31 - colBit)))
            {
                drawPixel(x + colBit,
                        y + row, col);
            }
            else drawPixel(x + colBit,
                           y + row, bgcol);
        }
    }
}


void drawBitmap1BPP(int x0, int y0, const unsigned char *bmp, int width, int height, uint32_t colorFG, uint32_t colorBG)
{
    int x, y;

    for (y = 0; y < height; y++)
    {
        for (x = 0; x < width; x++)
        {
            int bytePos = (y * (width/8)) + (x / 8);
            int bitPos = 7 - (x & 7);

            if (bmp[bytePos] & (1 << bitPos))
                drawPixel(x0 + x, y0 + y, colorFG);
            else
                drawPixel(x0 + x, y0 + y, colorBG);
        }
    }
}

