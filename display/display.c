void InitSpeedometerDisplay(void)
{
    /* Set background to BURNT_ORANGE */
    int x, y;
    enum colors color = BURNT_ORANGE;

    window_set(0, 0, MAX_X - 1, MAX_Y - 1);
    write_command(0x2C);
    for (x = 0; x < MAX_X; x++) {
        for (y = 0; y < MAX_Y; y++) {
            write_data((color >> 16) & 0xff);
            write_data((color >> 8) & 0xff);
            write_data((color) & 0xff);
        }
    }

    /* Draw static frame elements */
    drawBox(190, 790, 195, 205, ORANGE);
    drawBox(190, 200, 195, 470, ORANGE);
    drawBox(540, 550, 195, 470, ORANGE);
    drawBox(350, 360, 195, 470, ORANGE);
}

void UpdateSpeedBars(uint32_t rpm, uint8_t *shadowArray, uint8_t *pictureArray, uint8_t startUp)
{
    uint32_t analogRPM;
    uint8_t drawSeg;
    int j;

    /* Map RPM to bar index (0-110)
     * 0-2000 RPM -> bars 0-20 (fine resolution for low speeds)
     * 2000-20000 RPM -> bars 20-110 (full range up to max) */
    if (rpm < 2000) {
        analogRPM = MAP(rpm, 0, 2000, 0, 20);
    } else {
        analogRPM = MAP(rpm, 2000, 20000, 20, 110);
    }

    /* Clamp to valid range */
    if (analogRPM > 110) analogRPM = 110;

    /* Update each bar segment */
    for (j = 0; j < 110; j++) {
        int xPos = (j * 7) + 20;
        int yPos = j * 8;

        if (j > analogRPM) drawSeg = 0;
        else drawSeg = 1;

        shadowArray[j] = drawSeg;

        if (j < 20) {
            if (shadowArray[j] != pictureArray[j]) {
                pictureArray[j] = shadowArray[j];
                if (drawSeg) {
                    drawBox(xPos, xPos + 5, 15 + (20 * 8) - yPos, 150 + 15 + (20 * 8) - yPos, ORANGE);
                } else {
                    drawBox(xPos, xPos + 5, 15 + (20 * 8) - yPos, 150 + 15 + (20 * 8) - yPos, WHITE);
                }
            }
            /* Label every 500 RPM in the 0-2000 range */
            if ((j * 100) % 5 == 0) {
                if (startUp) {
                    drawNumber16x24(xPos, 5 + 150 + 15 + (20 * 8) - yPos, j * 100, ORANGE, BURNT_ORANGE);
                }
            }
        } else {
            if (shadowArray[j] != pictureArray[j]) {
                pictureArray[j] = shadowArray[j];
                if (drawSeg) {
                    drawBox(xPos, xPos + 5, 15, 150 + 15, ORANGE);
                } else {
                    drawBox(xPos, xPos + 5, 15, 150 + 15, WHITE);
                }
            }
            /* Label every 2000 RPM in the 2000-20000 range */
            if (((j - 20) * 200) % 2000 == 0) {
                if (startUp) {
                    drawNumber16x24(xPos, 2 + 150 + 15, 2000 + ((j - 20) * 200), ORANGE, BURNT_ORANGE);
                }
            }
        }
    }
}

